package com.soket.soketmobilecollector;

public class clsMutasiPinjaman {
    private String PRDNo;
    private String Tanggal;
    private String AngsPokok;
    private String AngsBunga;
    private String Denda;
    private String SisaPinjaman;

    public String getPRDNo() {
        return PRDNo;
    }
    public void setPRDNo(String prdNo) {
        PRDNo = prdNo;
    }

    public String getTanggal() {
        return Tanggal;
    }
    public void setTanggal(String tanggal) {
        Tanggal = tanggal;
    }

    public String getAngsPokok() {
        return AngsPokok;
    }
    public void setAngsPokok(String angsPokok) {
        AngsPokok = angsPokok;
    }

    public String getAngsBunga() {
        return AngsBunga;
    }
    public void setAngsBunga(String angsBunga) {
        AngsBunga = angsBunga;
    }

    public String getDenda() {
        return Denda;
    }
    public void setDenda(String denda) {
        Denda = denda;
    }

    public String getSisaPinjaman() {
        return SisaPinjaman;
    }
    public void setSisaPinjaman(String sisaPinjaman) {
        SisaPinjaman = sisaPinjaman;
    }
}
